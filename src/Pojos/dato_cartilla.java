public class dato_cartilla {

    private int id;
    private String id_cartilla;
    private String pos_i;
    private String pos_j;
    private String valor;

    public dato_cartilla() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getId_cartilla() {
        return id_cartilla;
    }

    public void setId_cartilla(String id_cartilla) {
        this.id_cartilla = id_cartilla;
    }

    public String getPos_i() {
        return pos_i;
    }

    public void setPos_i(String pos_i) {
        this.pos_i = pos_i;
    }

    public String getPos_j() {
        return pos_j;
    }

    public void setPos_j(String pos_j) {
        this.pos_j = pos_j;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
}
